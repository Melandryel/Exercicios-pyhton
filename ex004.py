s = str(input('Digite algo: '))
print('O tipo desse valor é', (type(s)))
print('É númerico?', s.isnumeric())
print('É alphanúmerico?', s.isalnum())
print('É alfabético?', s.isalpha())
print('Está em maiusculas?', s.isupper())
print('Está em minúsculas?', s.islower())
print('Está Capitalizada?', s.istitle())
print('tem espaços?', s.isspace())


